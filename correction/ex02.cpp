#include <iostream>

int main() {
    int a = 5;
    float b = 3.14;
    char c = 'A';
    bool d = true;
    std::cout << "int a: " << a << std::endl;
    std::cout << "float b: " << b << std::endl;
    std::cout << "char c: " << c << std::endl;
    std::cout << "bool d: " << d << std::endl;
    return 0;
}
